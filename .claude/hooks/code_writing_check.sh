            echo -e "${RED}â•‘   ðŸ›‘ Claude Enhancer Phase Enforcement - HARD BLOCK       â•‘${NC}" >&2
            echo -e "${RED}â•šâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•${NC}" >&2
            echo "" >&2
            echo -e "${YELLOW}âŒ Direct Write/Edit blocked in $current_phase${NC}" >&2
            echo "" >&2
            echo -e "${BLUE}ðŸ“ Current Status:${NC}" >&2
            echo "   Phase: $current_phase" >&2
            echo "   File: $FILE_PATH" >&2
            echo "   Tool: $TOOL_NAME" >&2
            echo "" >&2
            echo -e "${RED}ðŸš« Phase 1-5 Rule: MUST use SubAgents (no direct coding)${NC}" >&2
            echo "" >&2
            echo -e "${GREEN}âœ… Correct Workflow:${NC}" >&2
            echo "   1. Use Task tool to call specialized SubAgents" >&2
            echo "   2. Minimum agent count:" >&2
            echo "      â€¢ Simple tasks: 4 agents" >&2
            echo "      â€¢ Standard tasks: 6 agents" >&2
            echo "      â€¢ Complex tasks: 8 agents" >&2
            echo "" >&2
            echo -e "${BLUE}ðŸ’¡ Recommended Agents for $current_phase:${NC}" >&2

            case "$current_phase" in
                Phase1|P1)
                    echo "   â€¢ requirements-analyst - Requirements analysis" >&2
                    echo "   â€¢ technical-writer - PLAN.md generation" >&2
                    echo "   â€¢ backend-architect - Architecture design" >&2
                    echo "   â€¢ api-designer - API planning" >&2
                    ;;
                Phase2|P2)
                    echo "   â€¢ backend-architect - Code structure design" >&2
                    echo "   â€¢ fullstack-engineer - Implementation" >&2
                    echo "   â€¢ test-engineer - Test planning" >&2
                    echo "   â€¢ technical-writer - Documentation" >&2
                    ;;
                Phase3|P3)
                    echo "   â€¢ test-engineer - Test implementation" >&2
                    echo "   â€¢ performance-engineer - Performance testing" >&2
                    echo "   â€¢ security-auditor - Security testing" >&2
                    echo "   â€¢ code-reviewer - Quality check" >&2
                    ;;
                Phase4|P4)
                    echo "   â€¢ code-reviewer - Code review" >&2
                    echo "   â€¢ security-auditor - Security audit" >&2
                    echo "   â€¢ technical-writer - REVIEW.md generation" >&2
                    echo "   â€¢ test-engineer - Review test coverage" >&2
                    ;;
                Phase5|P5)
                    echo "   â€¢ technical-writer - Documentation updates" >&2
                    echo "   â€¢ devops-engineer - Release preparation" >&2
                    echo "   â€¢ monitoring-specialist - Monitoring setup" >&2
                    ;;
            esac

            echo "" >&2
            echo -e "${YELLOW}ðŸ“‹ How to fix:${NC}" >&2
            echo "   Instead of direct Write/Edit, use:" >&2
            echo "   Task(subagent_type='agent-name', prompt='...')" >&2
